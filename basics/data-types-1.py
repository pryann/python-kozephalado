# Numeric types: int, float, complex
# String: str
# Boolean: bool
# None type: NoneType

print('type of 10: ',  type(10))
print('type of 10.10: ',  type(10.10))
print('type of 10+20j: ',  type(10+20j))
print('type of \'banana\': ', type('banana'))
# print("type of 'banana': ", type('banana'))
# print("type of \"banana\": ", type('banana'))
print('type of "banana": ', type('banana'))
print('type of True: ', type(True))
print('type of None: ', type(None))
